#include "rtv1.h"

void	ft_normalize_vec(double (*vec)[3])
{
	double	norm;

	norm = sqrt((*vec)[0] * (*vec)[0] + (*vec)[1] * (*vec)[1] +
			(*vec)[2] * (*vec)[2]);
	(*vec)[0] = (*vec)[0] / norm;
	(*vec)[1] = (*vec)[1] / norm;
	(*vec)[2] = (*vec)[2] / norm;
}
